#utilizando a estrutura do programa interior (exemplo 14), some dos dois valores se forem diferentes e multiplique-os se forem iguais

n1 = int(input("informe um numero inteiro: "))
n2 = int(input("informe um numero inteiro: "))
if n1 == n2:
   print("Os valores são iguais, então o produto é ", n1 * n2)
else:
   print("Os valores são diferentes, então a soma é ", n1 + n2)







